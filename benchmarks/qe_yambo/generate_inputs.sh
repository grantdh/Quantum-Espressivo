#!/usr/bin/env bash
# generate_inputs.sh — produce clean SCF decks for the A/B/C BLAS benchmark.
#
# Why generate rather than reuse existing ~/qe-test/benchmark/siN.in?
# The legacy inputs use PAW pseudos, various ecutwfc, mixed k-grids — they were
# tuned for different physics studies. For a BLAS dispatch benchmark we need
# controlled inputs where the only things varying are system size and nbnd.
# Same pseudo (SG15 ONCV PBE), same ecutwfc, same conv_thr across all systems.
#
# Systems (diamond Si, conventional 8-atom cubic cell, a=10.26 bohr, tiled):
#   si64        — 2x2x2 conventional =  64 atoms, nbnd=200, k=2x2x2
#   si64_500b   — 2x2x2 conventional =  64 atoms, nbnd=500, k=1x1x1  (big matrices)
#   si216       — 3x3x3 conventional = 216 atoms, nbnd=500, k=1x1x1  (stress)
#
# Output: inputs/<system>/scf.in
# Requires: bash 4+ (uses arrays); works with macOS /bin/bash and Homebrew bash.
set -euo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${HERE}/config.sh"

# --- preflight -----------------------------------------------------------
if [[ ! -f "${PSEUDO_DIR}/${PSEUDO_SI}" ]]; then
    echo "${C_RED}ERROR${C_RST}: pseudo not found: ${PSEUDO_DIR}/${PSEUDO_SI}" >&2
    echo "       set PSEUDO_DIR / PSEUDO_SI in env or config.sh" >&2
    exit 1
fi

mkdir -p "${BENCH_INPUT_DIR}"

# --- Si diamond — 8-atom conventional cubic cell (fractional coords) ------
# Std diamond basis in the 8-atom conventional cell:
#   FCC sites           : (0,0,0) (1/2,1/2,0) (1/2,0,1/2) (0,1/2,1/2)
#   tetrahedral sites   : (1/4,1/4,1/4) and FCC-tiled
declare -a SI_FRAC=(
    "0.000 0.000 0.000"
    "0.500 0.500 0.000"
    "0.500 0.000 0.500"
    "0.000 0.500 0.500"
    "0.250 0.250 0.250"
    "0.750 0.750 0.250"
    "0.750 0.250 0.750"
    "0.250 0.750 0.750"
)

# --- write_input <system> <Nx> <Ny> <Nz> <nbnd> <kx> <ky> <kz> ------------
# Produces a self-contained scf.in in inputs/<system>/.
ALAT_BOHR=10.26
write_input() {
    local sys="$1" Nx="$2" Ny="$3" Nz="$4" nbnd="$5" kx="$6" ky="$7" kz="$8"
    local dir="${BENCH_INPUT_DIR}/${sys}"
    local out="${dir}/scf.in"
    mkdir -p "${dir}"

    local nat=$((8 * Nx * Ny * Nz))

    {
        cat <<EOF
! Generated by generate_inputs.sh on $(ts)
! System: ${sys} — Si diamond, ${Nx}x${Ny}x${Nz} of conventional cell (a=${ALAT_BOHR} bohr)
! Purpose: apple-bottom A/B/C BLAS benchmark (AB_MODE={cpu,gpu,auto})
&CONTROL
    calculation      = 'scf'
    prefix           = '${sys}'
    outdir           = './tmp'
    pseudo_dir       = '${PSEUDO_DIR}'
    verbosity        = 'default'
    tstress          = .false.
    tprnfor          = .false.
/
&SYSTEM
    ibrav            = 0
    nat              = ${nat}
    ntyp             = 1
    ecutwfc          = 25.0
    nbnd             = ${nbnd}
    occupations      = 'smearing'
    smearing         = 'gaussian'
    degauss          = 0.01
/
&ELECTRONS
    electron_maxstep = 50
    conv_thr         = 1.0d-6
    mixing_beta      = 0.3
    diagonalization  = 'david'
/
ATOMIC_SPECIES
  Si  28.0855  ${PSEUDO_SI}

CELL_PARAMETERS {bohr}
EOF
        awk -v a="${ALAT_BOHR}" -v Nx="${Nx}" -v Ny="${Ny}" -v Nz="${Nz}" \
            'BEGIN { printf "  %12.6f  %12.6f  %12.6f\n", a*Nx, 0, 0;
                     printf "  %12.6f  %12.6f  %12.6f\n", 0, a*Ny, 0;
                     printf "  %12.6f  %12.6f  %12.6f\n", 0, 0, a*Nz; }'

        echo ""
        echo "ATOMIC_POSITIONS {crystal}"
        # Tile the 8-atom basis across Nx*Ny*Nz copies, scale by 1/N per axis.
        for ((ix = 0; ix < Nx; ix++)); do
            for ((iy = 0; iy < Ny; iy++)); do
                for ((iz = 0; iz < Nz; iz++)); do
                    for f in "${SI_FRAC[@]}"; do
                        awk -v b="${f}" -v ix="${ix}" -v iy="${iy}" -v iz="${iz}" \
                            -v Nx="${Nx}" -v Ny="${Ny}" -v Nz="${Nz}" \
                            'BEGIN { split(b, a, " ");
                                     printf "  Si  %10.6f  %10.6f  %10.6f\n",
                                            (a[1]+ix)/Nx, (a[2]+iy)/Ny, (a[3]+iz)/Nz }'
                    done
                done
            done
        done
        echo ""
        echo "K_POINTS {automatic}"
        echo "  ${kx} ${ky} ${kz}  0 0 0"
    } > "${out}"

    echo "${C_GRN}[ok]${C_RST} wrote ${out}  (nat=${nat}, nbnd=${nbnd}, k=${kx}x${ky}x${kz})"
}

# --- benchmark system list -----------------------------------------------
write_input  si64        2 2 2  200  2 2 2
write_input  si64_500b   2 2 2  500  1 1 1
write_input  si216       3 3 3  500  1 1 1

echo ""
echo "${C_BLD}Done.${C_RST} Inputs in ${BENCH_INPUT_DIR}/"
echo "Next: ./bench_qe.sh si64   (or ./run_all.sh for the full suite)"
